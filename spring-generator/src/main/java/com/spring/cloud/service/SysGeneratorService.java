package com.spring.cloud.service;

import com.spring.cloud.dao.GeneratorDao;
import com.spring.cloud.utils.GenUtils;
import com.spring.cloud.utils.PageUtils;
import com.spring.cloud.utils.Query;
import org.apache.commons.io.IOUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.io.ByteArrayOutputStream;
import java.util.List;
import java.util.Map;
import java.util.zip.ZipOutputStream;

/**
 * 代码生成器
 *
 * @author chenshun
 * @email sunlightcs@gmail.com
 * @date 2016年12月19日 下午3:33:38
 */
@Service
public class SysGeneratorService {

    @Autowired
    private GeneratorDao generatorDao;

    public PageUtils queryList(Query query) {
/*        IPage<?> page = this.baseMapper.selectPage(
                new com.jinshe.exchange.util.Query<>(query).getPage(),
                new QueryWrapper<>()
        );*/
        List<Map<String, Object>> list = generatorDao.queryList(query);

        return new PageUtils(list, 900, query.getLimit(), query.getPage());
    }

    public Map<String, String> queryTable(String tableName) {
        return generatorDao.queryTable(tableName);
    }

    public List<Map<String, String>> queryColumns(String tableName) {
        return generatorDao.queryColumns(tableName);
    }

    public byte[] generatorCode(String[] tableNames) {
        ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
        ZipOutputStream zip = new ZipOutputStream(outputStream);

        for (String tableName : tableNames) {
            //查询表信息
            Map<String, String> table = queryTable(tableName);
            //查询列信息
            List<Map<String, String>> columns = queryColumns(tableName);
            //生成代码
            GenUtils.generatorCode(table, columns, zip);
        }
        IOUtils.closeQuietly(zip);
        return outputStream.toByteArray();
    }
}
